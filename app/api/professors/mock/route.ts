export async function POST(request: Request) {
  const searchParams = new URL(request.url).searchParams
  const university = searchParams.get('university')
  
  let results = []
  let message = ''
  
  if (!university) {
    // Return all professors if no university specified
    results = [
      ...REAL_PUCMM_PROFESSORS, ...ADDITIONAL_PUCMM_PROFESSORS,
      ...REAL_INTEC_PROFESSORS, ...ADDITIONAL_INTEC_PROFESSORS, ...ADDITIONAL_INTEC_PROFESSORS_FROM_SCREENSHOTS,
      ...INTEC_HEALTH_SCIENCES_PROFESSORS, ...INTEC_SOCIAL_SCIENCES_PROFESSORS, ...INTEC_ECONOMICS_BUSINESS_PROFESSORS, ...INTEC_ENGINEERING_PROFESSORS,
      ...REAL_UTESA_PROFESSORS, ...REAL_UASD_PROFESSORS, ...REAL_UNIBE_PROFESSORS, ...REAL_UNPHU_PROFESSORS, ...BARNA_PROFESSORS
    ]
    message = 'Real scraped data - 70 PUCMM + 187 INTEC + 10 UTESA + 5 UASD + 5 UNIBE + 116 UNPHU + 16 BARNA professors'
  } else if (university.toUpperCase() === 'PUCMM') {
    results = [...REAL_PUCMM_PROFESSORS, ...ADDITIONAL_PUCMM_PROFESSORS]
    message = 'Real scraped data - 70 PUCMM professors'
  } else if (university.toUpperCase() === 'INTEC') {
    results = [
      ...REAL_INTEC_PROFESSORS, 
      ...ADDITIONAL_INTEC_PROFESSORS, 
      ...ADDITIONAL_INTEC_PROFESSORS_FROM_SCREENSHOTS,
      ...INTEC_HEALTH_SCIENCES_PROFESSORS,
      ...INTEC_SOCIAL_SCIENCES_PROFESSORS,
      ...INTEC_ECONOMICS_BUSINESS_PROFESSORS,
      ...INTEC_ENGINEERING_PROFESSORS
    ]
    message = 'Real scraped data - 187 INTEC professors from all departments'
  } else if (university.toUpperCase() === 'UTESA') {
    results = REAL_UTESA_PROFESSORS
    message = 'Real scraped data - 10 UTESA professors'
  } else if (university.toUpperCase() === 'UASD') {
    results = REAL_UASD_PROFESSORS
    message = 'Real scraped data - 5 UASD professors'
  } else if (university.toUpperCase() === 'UNIBE') {
    results = REAL_UNIBE_PROFESSORS
    message = 'Real scraped data - 5 UNIBE professors'
  } else if (university.toUpperCase() === 'UNPHU') {
    results = REAL_UNPHU_PROFESSORS
    message = 'Real scraped data - 116 UNPHU professors'
  } else if (university.toUpperCase() === 'BARNA') {
    results = BARNA_PROFESSORS
    message = 'Real scraped data - 16 BARNA professors'
  } else {
    // Check if it's one of the other Dominican universities
    const universityData = DOMINICAN_UNIVERSITIES.find(uni => 
      uni.shortName.toUpperCase() === university.toUpperCase() || 
      uni.id.toUpperCase() === university.toUpperCase()
    )
    
    if (universityData) {
      results = []
      message = `${universityData.shortName} is a recognized Dominican university but no professors have been scraped yet. Students can add professors using the "Agregar Profesor" feature.`
    } else {
      results = []
      message = `University "${university}" not found in Dominican Republic universities database`
    }
  }

  return NextResponse.json({
    success: true,
    results,
    total: results.length,
    message,
    universities: DOMINICAN_UNIVERSITIES.map(uni => ({
      id: uni.id,
      name: uni.name,
      shortName: uni.shortName,
      location: uni.location,
      professorCount: 
        uni.id === 'pucmm' ? 70 :
        uni.id === 'intec' ? 187 :
        uni.id === 'utesa' ? 10 :
        uni.id === 'uasd' ? 5 :
        uni.id === 'unibe' ? 5 :
        uni.id === 'unphu' ? 116 :
        uni.id === 'barna' ? 16 :
        0
    }))
  })
}

